bool Database::checkLifeSignal(unsigned short lifeSignal)
{
    static unsigned short int temp = 0;
    static int counter = 0;
    if (temp == lifeSignal)
    {
        counter --;
    }

    else
    {
        counter ++;
    }

    temp = lifeSignal;
    if (counter >= 10)
    {
        counter = 10;
        return true;
    }
    else if (counter <=0)
    {
        counter = 0;
        return false;
    }
    else
    {
        return false;
    }
}

